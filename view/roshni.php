                <p>'ROSHNI' is a green innovative concept, evolved at the behest of the President of India, encompassing environment management and empowerment of local communities to create eco-friendly sustainable urban habitats through participation of stakeholders and convergence of partnership programmes. It works towards a holistic development of habitats.</p>

                <h5>Environment Management Initiatives</h5> 
                <ul class="list1">
    <li> Improvement of the Raj Bhavan garden</li>
    <li> Community Empowerment</li>
    <li> Waste management</li>
    <li> Energy conservation</li>
    <li> Use of alternate source of energy</li>
    <li>Rain water harvesting</li>
    <li>Gender empowerment</li>
    </ul>             
        <h5>Community Empowerment</h5> 
        <p>It is to create eco-friendly sustainable urban habitat. Plantation drives are run round the year and ‘Vana Mahotshav’ (July 1-7), World Earth Day (April 22) and World Environment Day (June 5) is sincerely observed.</p>
                          <ul class="list1">
    <li> NGOs and public celebrities participate in plantation drives and awareness programmes held in Raj Bhavan.</li>
    <li> Annual competition held among Raj Bhavan Colony inmates for Clean & Green Campus Award.</li>
    <li> Visiting dignitaries usually presented with saplings instead of bouquets as a token of our moral commitment to keeping the environment green.</li>
    <li>Yoga and ayurveda camps organised to create awareness about the medicinal value of trees.</li>
    </ul>
    <h5>Waste Management</h5>
                    <ul class="list1">
    <li> Dry leaf and other organic wastes are utilised for composting.</li>
    <li> Vermi-compost unit, comprising a 5m x 2m x 1m pit, has been set up and is functional since 5th May 2011; subsequently two more units were commissioned in 2012.</li>
    <li> Around 15% of total manure requirement is met from leaf mould and animal waste.</li>
    <li>A bio-gas unit, linked to the dairy unit, is functional.</li>
    <li>Raj Bhavan campus is plastic-free and all plastic wastes are isolated and dispatched on a daily basis.</li>
    </ul>
    <h5>Energy Conservation</h5>
            <ul class="list1">
    <li>Energy Audit conducted last year.</li>
    <li>As per recommendation of the Energy Audit Team, false ceiling provided in rooms for minimising use of A/C machines and conventional electrical gadgets. This will be replaced by energy efficient devices.</li>
    <li> Staff sensitised during meetings for austerity measures.</li>
    <li>World Earth Hour observed to create awareness among staff and general public for conservation of energy.</li>
    </ul>
    <h5>Use of alternate source of energy</h5>
      <ul class="list1">
    <li>Renewable Energy Demonstration Project taken up at Raj Bhavan by OREDA through assistance of MNRE, Government of India for the year 2011-12.</li>
    <li>Objective: Publicity of renewable energy technologies as well as dissemination of information on technological development and promotional activities.</li>
    <li>Project cost: Rs.1.9 crore with GoI share of Rs.1 crore towards complete installation and maintenance of the plant for first five years.</li>
    <li>Setting up of a grid interactive 30 kWp solar power plant to cater to the conventional energy requirement of evening peripheral load as well as that of the Administrative Block with renewable, clean and silent energy.</li>
    <li>Partial replacement of power consuming lighting gadgets with the latest energy efficient LED lights.</li>
    <li>Two battery operated golf cart type vehicles for conveyance within the campus with clean energy.</li>
    <li>Use of solar water heating system of 1000 LPD capacity.</li>
    <li>One small household type bio-gas plant.</li>
    <li>Projected savings from power generation per annum: Rs. 1,35,000/-</li>
    <li>Possible savings in energy bill in case of LED replacement per annum: Rs. 60,000/-</li>
    </ul>
    <h5>Rain water harvesting</h5>
                  <ul class="list1">
    <li>Rain water harvesting structure installed at Raj Bhavan on 3rd August' 2002 by the Central Ground Water Board.</li>
    <li>Project cost: Rs. 14.17 lakh.</li>
    <li> Three trenches (1m x 1m) with lined wall totaling 525 meters and open trench of 75 meters in length dug to harvest the surface run off with a view to recharge the phreatic aquifer as well as the deeper aquifer.</li>
    <li>Two filter chambers to remove impurities from the run off.</li>
    <li>Three 80-meter-deep bore-wells, one each at the terminal point of each trench system, to guide the harvested water to deeper aquifer.</li>
    </ul>
