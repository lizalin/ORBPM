  			<h5>Administration of Scheduled Areas </h5>

  			<p>The Governor of Odisha has special responsibility about the administration of Scheduled Areas are provided in the Fifth Schedule of the Constitution.</p>
  			<p>The Scheduled Areas Order 1977 declares three full districts viz. Mayurbhanj, Sundargarh and Koraput (after re-organization, 7 districts viz. Mayurbhanj, Sundargarh, Jharsuguda, Koraput, Malkangiri, Rayagada and Nawarangpur) and parts of other districts namely Kuchinda tehsil of Sambalpur district, Keonjhar, Telkoi, Champua and Barbil tehsil of Keonjhar district, Kandhamal, Baliguda and G.Udayagiri tehsil of Kandhamal district, R. Udayagiri tehsil (including present Mohana tehsil), Guma and Rayagada Blocks in Paralakhemundi sub-division of Gajapati district and Suruda tehsil excluding Gajalbadi and Gochha gram panchayat of Ghumsar Sub-division of Ganjam district, Thuamul-Rampur and Lanjigarh of Kalahandi district and Nilagiri Block of Balasore district as Scheduled Areas of the State.</p>
  			<ul>
  				<li>
  					<h5>Article 338 A (7)</h5>
  				</li>
  				<p>Where any such report of National Commission for Scheduled Tribes, or any part thereof, related to any matter with which any State Government is concerned, a copy of such report shall be forwarded to the Governor of the State who shall cause it to be laid before the Legislature of the State along with a recommendation explaining the action taken or proposed to be taken on the recommendations relating to the State and the reasons for the non-acceptance, if any, of any such recommendations.</p>
  			</ul>
  			<ul>
  				<li>
  					<h5>Article 361</h5>
  				</li>
  				<p>(1) The Chairman and other members of the Public Service Commission shall be appointed in the case of Union Commission or Joint Commission by the President and in the case of a State Commission by the Governor of the State.</p>
  				<p>(2) The President, or the Governor of a State shall not be answerable to any Court for the exercise and performance of the powers and duties of his office or for any act done or purporting to be done by him in the exercise and performance of those powers and duties;</p>
  				<p>Provided that the conduct of the President may be brought under review by any Court, tribunal or body appointed or designated by either House of Parliament for the investigation of a charge under Article 61;</p>
  				<p>Provided further that nothing in this clause shall be construed as restricting the right of any person to bring appropriate proceedings against the Government of India or the Government of a State:</p>
  				<p>(1) No criminal proceedings, whatsoever, shall be instituted, or continued against the President, or the Governor of a State, in any Court during his term of office.</p>
  				<p>(2) No process for the arrest or imprisonment of the President, or the Governor of a State, shall issue from any Court during his term of office.</p>
  			</ul>

