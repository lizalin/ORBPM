  			<h5>As Constitutional Head</h5>
  			<p>Relevant provisions of the Constitution of India on Governor and his functions:</p>
  			<ul>
  				<li>
  					<h5>Article 151 (2)</h5>
  				</li>
  				<p>The report of the Comptroller and Auditor General of India relating that accounts of the State shall be submitted to the Governor, who shall cause them to be laid before the legislation of the State.</p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 153</h5>
  				</li>
  				<p>Governors of States:- There shall be a Governor for each state:</p>
  				<p>[Provided that nothing shall prevent the appointment of the same person as Governor for two or more states.]</p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 154</h5>
  				</li>
  				<p>Executive power of State. - The Executive power of the state shall be vested in the Governor and shall be exercised by him either directly or through officers subordinate to him in accordance with this constitution.</p>
  				<p>(2) Nothing in this article shall-</p>
  				<p>(a) Be deemed to transfer to the Governor any functions conferred by any existing law on any other authority; or</p>
  				<p>(b) prevent Parliament or the Legislature of the State from conferring by law functions on any authority subordinate to the Governor .</p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 155</h5>
  				</li>
  				<p>Appointment of Governor- The Governor of a State shall be appointed by the President by warrant under his hand and seal.</p>
  			</ul>


  			<ul>
  				<li>
  					<h5>Article 156 </h5>
  				</li>
  				<p>Term of office of Governor</p>
  				<p>(1) The Governor shall hold office during the pleasure of the President.</p>
  				<p>(2) The Governor may, by writing under his hand addressed to the President, resign his office. </p>
  				<p>(3) Subject to the foregoing provisions of this article, a Governor shall hold for a term of five years from the date on which he enters upon his office. </p>
  				<p> Provided that a Governor shall, notwithstanding the expiration of his term, continue to hold office until his successor enters upon his office. </p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 157</h5>
  				</li>
  				<p>Qualification for appointment as Governor- No person shall be eligible for appointment as Governor unless he is a citizen of India and has completed the age of thirty-five years.</p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 158 </h5>
  				</li>
  				<p>Condition of Governor's Office </p>
  				<p>(1) The Governor shall not be a member of either House of Parliament or of a House of the Legislator of any State specified in the First Schedule, and if a member of either House of Parliament or of a House of the Legislature of any such State be appointed Governor, he shall be deemed to have vacated his seat in that House or the date on which he enters upon his office as Governor. </p>
  				<p>(2) The Governor shall not hold any other office of profit.</p>
  				<p>(3) The Governor shall be entitled without payment of rent to the use of his official residences and shall be also entitled to such emoluments, allowances and privileges as may be determined by Parliament by law and until provision in that behalf is so made, such emoluments, allowances and privileges as are specified in the Second Schedule. </p>
  				<p>[3A] where the same person is appointed as Governor of two or more States, the emoluments and allowances payable to the Governor shall be allocated among the States in such proportion as the President may by order determine.] </p>
  				<p>(4) The emoluments and allowances of the Governor shall not be diminished during his term of office. </p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 159</h5>
  				</li>
  				<p>Oath or affirmation by Governor- Every Governor and every person discharging the functions of the Governor shall, before entering his office, make and subscribe in the presence of the Chief Justice of the High Court exercising jurisdiction in relation to the State, or, in his absence, the senior most Judge of that Court available, on oath or affirmation in the following form, that is to say- </p>
  				<p> I,A.B., do swear in the name of God/ solemnly affirm, that I will faithfully execute the office of Governor (or discharge the functions of the Governor) of .......(name of the State) and will to the best of my ability preserve, protect and defend the Constitution and the law and that I will devote myself to the service and well-being of the people of .......(name of the State)." </p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 160</h5>
  				</li>
  				<p>Discharge of the functions of the Governor in certain contingencies- The President may make such provision as he thinks fit or the discharge of the functions of the Governor of a State in any contingency not provided for this Chapter. </p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 161</h5>
  				</li>
  				<p>Power of governor to grant pardons, etc., and to suspend, remit or commute sentences in certain cases:</p>
  				<p>The Governor of a State shall have the power to grant pardons, reprieves, respites or remissions of punishment or to suspend, remit or commute the sentence of any person convicted of any offence against any law relating to a matter to which the executive power of the State extends. </p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 163</h5>
  				</li>
  				<p>(1) There shall be a council of Ministers with the Chief Minister at the head to aid and advise the Governor in the exercise of his functions, except in so far as he is by or under this Constitution required to exercise his functions or any of them in his discretion.</p>
  				<p>(2) If any question arises whether any matter is or is not a matter as respects which the Governor is by or under this Constitution requires to act I his discretion, the decision of the Governor in his discretion shall be final and the validity of anything done by the Governor shall not be called in question on the ground that he ought or ought not to have acted in his discretion.</p>
  				<p>(3) The question whether any, and if so what, advice was tendered by the Ministers to the Governor shall not be inquired into in any Court.</p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 164</h5>
  				</li>
  				<p>(1) The Chief Minister shall be appointed by the Governor and other Ministers shall be appointed by the Governor on the advice of the Chief Minister, and the Ministers shall hold office during the pleasure of the Governor;</p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 166</h5>
  				</li>
  				<p>(1) All executive action of the Government of a State shall be expressed to be taken in the name of the Governor.</p>
  				<p>(2) Orders and other instruments made and executed in the name of the Governor shall be authenticated in such matter as may be specified in rules to be made by the Governor, and the validity of an order or instrument which is so authenticated shall not be called in question on the ground that it is not an order or instrument made or executed by the Governor.</p>

  			</ul>


  			<ul>
  				<li>
  					<h5>Article 167</h5>
  				</li>
  				<p>It shall be the duty of the Chief Minister of each State:-</p>
  				<p>(a) to communicate to the Governor of the State all decisions of the council of Ministers relating to the administration of the affairs of the State and proposals for legislation;</p>
  				<p>(b) to furnish such information relating to the administration of the affairs of the State and proposals for legislations the Governor may call for; and</p>
  				<p>Â© if the Governor so requires to submit for the consideration of the Council of Ministers any matter on which a decision has been taken by a Minister but which has not been considered by the Council.</p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 168</h5>
  				</li>
  				<p>(1)For every state there shall be a Legislature which shall consist of the Governor - .and</p>
  				<p>(a) In the states of Bihar, Maharashtra, Karnataka and Uttar Pradesh, two Houses;</p>
  				<p>(b) In other States, one House.</p>
  			</ul>


  			<ul>
  				<li>
  					<h5>Article 174</h5>
  				</li>
  				<p>(1)The Governor shall from time to time summon the House or each House of the Legislature of the State to meet at such time and place as he thinks fit, but six months shall not intervene between its last sitting in one session and the date appointed for its first sitting in the next session.</p>
  				<p>(2)The Governor may from time to time:-</p>
  				<p>(a)prorogue the House or either House;</p>
  				<p>(b) dissolve the Legislative Assembly.</p>
  			</ul>


  			<ul>
  				<li>
  					<h5>Article 175</h5>
  				</li>
  				<p>(1)The Governor may address the Legislative Assembly or, in the case of a State having a Legislative Council, either House of the Legislature of the State, or both Houses assembled together, and may for that purpose require the attendance of members.</p>
  				<p>(2)The Governor may send messages to the House or Houses of the Legislature of the State, whether with respect to a Bill then pending in the Legislature or otherwise, and a House to which any message is so sent shall with all convenient despatch consider any matter required by the message to be taken into consideration.
  				</p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 176</h5>
  				</li>
  				<p>(1)At the commencement of (the first session after each general election to the Legislative Assembly ads at the commencement of the first session of leach year), the Governor shall address, the Legislative Assembly or, in the case of a State having a Legislative Council, both Houses assembled together and inform the Legislature of the causes of its summons.</p>
  				<p>Provision shall be made by the rules regulating the procedure of the House or either House for the allotment of time for discussion of the matters referred to in such address.</p>

  			</ul>


  			<ul>
  				<li>
  					<h5>Article 200</h5>
  				</li>
  				<p>When a Bill has been passed by the Legislative Assembly of a State or in the case of a State having a Legislative Council, has been passed by both Houses of the Legislature of the State, it shall be presented to the Governor and the Governor shall declare either that he assents to the Bill or that he withholds assent therefrom or that he reserves the Bill for the consideration of the President;</p>
  				<p>Provided that the Governor may, as soon as possible after the presentation to him of the Bill for assent, return the Bill if it is a Money Bill together with a message requesting that the House or Houses will reconsider the Bill or any specified provisions thereof and, in particular, will consider the desirability of introducing any such amendments as he may recommend in his message and, when a Bill is so returned, the House or Houses shall reconsider the Bill accordingly, and if the Bill is presented to the Governor for assent, the Governor shall not withhold assent therefrom;</p>
  				<p>Provided further that the Governor shall not assent to, but shall reserve for the reconsideration of the President, any Bill which in the opinion of the Governor would, if it became law, so derogate from the powers of the High Court as to endanger the position which that Court is by this Constitution designed to fill.</p>
  			</ul>



  			<ul>
  				<li>
  					<h5>Article 201</h5>
  				</li>
  				<p>When a Bill is reserved by a Governor for the consideration of the President, the President shall declare either that he assents to the Bill or that he withholds assent therefrom;</p>
  				<p>Provided that, where the bill is not a Money Bill, the President may direct the Governor to return the Bill to the House or, as the case may be, the Houses of the Legislature of the State together with such a message as is mentioned in the first proviso to Article 200 and, when a Bill is so returned, the House or Houses shall reconsider it accordingly within a period of six months from the date of receipt of such message and, if it is again passed by the House or Houses with or without amendment, it shall be presented again to the President for his consideration.</p>

  			</ul>

  			<ul>
  				<li>
  					<h5>Article 202</h5>
  				</li>
  				<p>The Governor shall in respect of every financial year cause to be laid before the House.....a statement of the estimated receipts and expenditure.</p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 203(3)</h5>
  				</li>
  				<p>No demand for a grant shall be made except on the recommendation of the Governor.</p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 205</h5>
  				</li>
  				<p>The Governor shall cause to be laid before the House another statement showing estimated amount of expenditure (Article 205).</p>
  				<p>Provided that no recommendation shall be required under this clause for the moving of an amendment making provision for the reduction or abolition of any tax.</p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 207</h5>
  				</li>
  				<p>(1) A Bill or amendment making provision for any of the matters specified in sub-clauses (a) to (f) of clause (1) of Article 199 shall not be introduced or moved except on the recommendations of the Governor, and a bill making such provision shall not be introduced in a Legislative Council.</p>
  				<p>Provided that no recommendation shall be required under this clause for the moving of an amendment making provision for the reduction or abolition of any tax.Article 213</p>
  				<p>The Governor may promulgate such Ordinances, as the circumstances appear to him to require.... during the recess of legislature.</p>
  			</ul>


  			<ul>
  				<li>
  					<h5>Article 217</h5>
  				</li>
  				<p>The Governor is consulted for appointment of Judges of High Court.</p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 219</h5>
  				</li>
  				<p>Every person appointed to be a Judge of a High Court shall, before he enters upon his office, make and subscribe before the Governor of the State, or some person appointed in behalf by him, an oath or affirmation according to the form set out for the purpose.</p>
  			</ul>

  			<ul>
  				<li>
  					<h5>Article 233 (1)</h5>
  				</li>
  				<p>Appointment of District Judges: </p>
  				<p>Appointment of persons to be, and the posting and promotion of, district judges in any State shall be made by Governor of the State in consultation with the High Court exercising jurisdiction in relation to such State. </p>
  			</ul>



