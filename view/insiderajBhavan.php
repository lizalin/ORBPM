                <p>The official chamber and residential quarters of His Excellency the Governor, office of the Secretary to Governor, office of the Aide-de-camp, Personal Secretary, Private Secretary and Comptroller, guest rooms, library, the Abhishek Hall, the Banquet Hall and a mini conference hall are located in the main building. Several artifacts comprising stone statues, pieces of filigree work, paintings, photographs and trophies adorn the Raj Bhavan.</p>
