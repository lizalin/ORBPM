                <h5>9. Garden</h5>
                <p>The Raj Bhavan garden boasts of lush green lawns, two rose gardens, a cactus house and flower beds along the main driveway where a variety of seasonal flowers are grown besides two gardens for medicinal herbs and a kitchen garden. Various types of seasonal flowers are planted on the flower beds. A large number of potted flower plants are also placed in the portico as well as in front of the main building.</p>
                <p>Apart from the flower plants, foliage plants like crotons, difanbachea, coleus, cycas, drasera, etc are grown in pots. Four fountains decorated with colourful lights beautify the lawn. The in-house nursery meets the major requirements of the garden. Varieties of vegetables are grown in the kitchen garden to cater to the requirements of the kitchen. Raj Bhavan garden has been adjudged the best garden in the city a number of times. In the Annual Flower Show and Garden Competition organised by Plant Lovers' Association in 2008, this garden won the best garden trophy among the gardens of central and state government offices in Bhubaneswar.</p>
