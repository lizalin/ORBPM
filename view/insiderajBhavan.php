                <h5>12. Deer Park</h5>
                <p>The deer park was inaugurated on 2nd October 1973 by His Excellency Shri B. D. Jatti.  Spread over a small area of one acre to the northeast of the premises, it originally housed a maximum of 10 animals including a pair of spotted deer and 3 black bucks. Subsequently, more animals were brought here from the Nandan Kanan Zoo. The southern wing, spreading over an area of one acre of land, was inaugurated by His Excellency Dr. Nurul Hasan, the then Governor, on 14th November 1989. It is now house to 80 spotted deer and 2 black bucks.</p>
