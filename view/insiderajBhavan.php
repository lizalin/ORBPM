                <h5>8. Administrative Building</h5>
                <p>The Governor's Secretariat is housed in the Administrative Block, which is located on the southeast side of the main building. The Secretariat functions under the administrative control of the Secretary to the Governor, who assists His Excellency in discharging his duties and functions. The administrative block houses a post office and a printing press in addition to the other administrative sections.</p>
