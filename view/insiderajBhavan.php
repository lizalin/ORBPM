                <h5>7. Residence of His Excellency</h5>
                <p>The residential quarters of His Excellency and his family are located on the first floor. The rooms and the corridors are tastefully decorated. There are nine suites in the main building for the visiting dignitaries. The suites are quite spacious, with a dressing room and a toilet. </p>
                <p>The suites are names after places of Odisha bearing historical and geographical importance. The three suites on the ground floor along the western arm of the building - Kalinga, Chilika and Pallavi - are meant for VVIPs and VIPs. The Kalinga suite consists of a drawing room, a bed room, a dining room, a mini pantry and an attendant's room. </p>
                <p>The other six suites, namely, Dhauli, Ansupa and Konark on the ground floor along the northern arm and Similipal, Chandragiri and Pipili on the first floor provide accommodation to other visiting dignitaries.</p>
                <p>Each suite is decorated with photographs and paintings.</p>
                <p>The Kalinga suite is decorated with a portrait of Baji Rout, the boatman boy, who used to ferry British soldiers across the river Brahmani and fell to their bullets.</p>
                <p>The Chilika suite has two photographs of Chilika, the largest brackish water lagoon in Asia. </p>
                <p>The picture of a danseuse in a particular pose signifies the name of the Pallavi suite. Paintings and photographs of Ansupa Lake, the Sun Temple of Konark and Chandragiri and an appliqué wall hanging of Pipili adorn the respective rooms bearing these names. Besides these, the three rooms in the guest annex provide lodging facilities to visitors.</p>
