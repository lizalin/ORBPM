                <h5>5. Mini Conference Hall</h5>
                <p>A mini conference hall with a capacity of about 20 persons, has been added to the main block. The seats surround an oval-shaped table and are equipped with microphones. The hall is equipped with state-of-the-art infrastructure including a computer and an overhead LCD projector with screen. His Excellency Dr. A.P.J Abdul Kalam, the then President of India, inaugurated the hall on 3rd July 2006.</p>
                 <div class="clearfix"></div>
                <h5>6. Library</h5>
                <p>The Raj Bhavan library consists of about 7000 books in different languages such as English, Oriya, Bengali, Hindi, Sanskrit, and Urdu. The collection includes books on the Constitution, and various other subjects such as religion, philosophy, history, culture, yoga, literature, economics, management and computer science. The library is used by the Governor, the staff of Raj Bhavan and their families. Guests, who visit the Raj Bhavan and stay there, can also use the library. The present library hall was inaugurated by His Excellency Shri M. M. Rajendran on 9th October 2002.</p>
