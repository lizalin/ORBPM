                <h5>1. The Raj Bhavan Complex</h5>   
                
                <p>The hilly terrain of the Raj Bhavan Complex has been terraced into several sections. On top of the hillock stands the magnificent main building, and the other buildings are located below it.</p>
                <p>The complex comprises of the main building, the administrative block, an annexe, the office of the reserve inspector, a reception counter, a dispensary, a garage and a godown. A portion of the premises is forested and the remaining area is covered with different horticulture species such as coconut, cashew, mango and other fruit bearing trees. Open lawns adorn the paths leading up to the buildings. </p>
