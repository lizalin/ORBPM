                <h5>10. Medicinal Gardens</h5>
                <p>Two medicinal gardens are also part of this huge foliage. The first one comprises 30 varieties of medicinal plants.  His Excellency Shri A.P.J. Abdul Kalam, the then President of India, planted a pengulai tree here on 3rd July, 2006. The second medicinal garden near the Governor’s Apartment comprises 32 species of plants. Former Hon’ble Prime Minister, Dr. Manmohan Singh planted a cinnamon tree here on 29th August, 2006.</p>
                <p>The medicinal herbs include manjuati, ruta, gheekuanri, hadajoda, pasaruni, peepali, patal gauda, ashoka, gogula and bhringaraj. A unique feature of the herbal garden is that there is a diagram of the human body, illustrating how different organs can medicially benefit from different herbs planted in the garden.</p>
