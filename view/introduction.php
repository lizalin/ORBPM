                <p>The three Raj Bhavans in Odisha - the summer resort at Puri, the Lal Bagh Palace at Cuttack and the Raj Bhavan at Bhubaneswar - present an interesting account of the evolution of modern Odisha. The Raj Bhavan of Puri served as the residence of the Governor of Odisha from 1936-1942 and the Lal Bagh Palace at Cuttack from 1942-1960. Finally, Raj Bhavan, Bhubaneswar continues to serve as the residence of the Governor the Odisha from 1960 till date. The first two were called Government Houses during the British rule and the last mentioned Raj Bhavan, Bhubaneswar is post-independence structure. The Raj Bhavan at Puri is still used by His Excellency during his visit to the city, but the one at Cuttack is now used as a pediatric hospital, popularly known as Shishu Bhavan.

