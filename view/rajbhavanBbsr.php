                 <p>The Raj Bhavan at Bhubaneswar is an important landmark of the modern capital city of Odisha. Situated on a hillock known as Bhalu Mundia, the building presents a magnificent sight. The construction of a residence for the Governor was taken up in a sprawling plot of land measuring 88 acres, which was then located in the western part of the existing township. Architect Shri Julius Vaz prepared the design.</p>
                <p>The foundation stone was laid by Dr. Harekrushna Mahatab, the then Chief Minister of Odisha. Construction work started on 1st January 1958 under the supervision of the Chief Engineer Shri K.K.Kartha, Superintending Engineers, Shri S. Behera and Shri S.R. Padhi and Executive Engineer Shri Mumtaz Ali. The construction was completed on 31st March, 1960. After the Raj Bhavan was properly furnished, Sri Y.N. Sukthankar, the 11th Governor of Odisha, occupied it. It has been the official accommodation of successive Governors of Odisha till date.</p>
