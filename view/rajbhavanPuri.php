                 <p>The Raj Bhavan at Puri, which was known as the Government House during the British rule, was constructed in the early part of the twentieth century. Soon after the Province of Bihar and Orissa was carved out of the Bengal Presidency in 1912, the administration of the newly formed province functioned at Ranchi. However, it was decided that Bankipore Patna would be the capital and Puri would be the summer capital of the province.</p>
                <p>The construction of the Government House at Puri commenced almost at the centre of the sprawling Balukhand Government Estate towards the later part of 1913 and was completed in 1914. During the period from 1914 to 1936, as many as ten Lieutenant Governors and Governors of Bihar and Odisha made an annual stay here during summer months.</p>
                <p>After Odisha was accorded the status of a separate province in 1936, Cuttack became its capital. The Government House at Puri then served as the interim residence of the Governor. All care was taken to make the building suitable and comfortable for the imperial dignity of His Majesty’s representative in the province. Later, several additional structures were built.</p>
                <p>After the Governor’s residence shifted to Cuttack in August 1942, the Government House at Puri served as the summer resort of the Governor. Successive Governors and their guests stayed here during their visits to Puri. Several dignitaries including His Excellency Lord Linlithgow and His Excellency Lord Mountbatten visited the Government House. “Puri sea beach is the best beach I have ever visited and the Puri Government House is the finest,” remarked Lord Mountbatten in 1948, as Smt Saroj Mukherjee, the daughter of His Excellency Dr. Kailash Nath Katju, recalls. Usually, when the guests of His Excellency visit Odisha, they make it a point to pay a visit to Puri.</p>
                <p>Originally, the Government House stood on an area measuring 30.226 acres, out of which a plot of land measuring 8.926 acres was alienated. Hotel Neelachal Ashok was built here in 1983 to give a boost to tourism in the state. The two-storeyed Puri Raj Bhavan now stands on an area of 21.30 acres comprising 11 suites including 4 VIP suites, a kitchen and a dining hall besides an office room, a reception hall and a sprawling verandah.</p>
