                <p>On the occasion of Independence Day and the Republic Day, His Excellency the Governor of Odisha hosts At Home party at the Raj Bhavan, Bhubaneswar. Dignitaries such as the Chief Minister, Chief Justice of Orissa High Court, Speaker of Odisha Legislative Assembly, Leader of Opposition, all the Ministers in the Council of Minister, all the Members of Parliament representing Odisha, Members of the Odisha Legislative Assembly, Judges of the Orissa High Court, Chairman and Member of Central Administrative Tribunal, State Administrative Tribunal, Freedom Fighters, Padma awardees, prominent industrialists and social workers of the State, chiefs of important NGOs, eminent persons in the field of art, literature, music, sports from the State, former Governors and Chief Ministers of Odisha, all the Union Ministers from Odisha, Vice-Chancellors of Universities in Odisha, awardees of prominent police, military, literature and sports medals from Odisha, former Judges of Odisha High Court, former Advocate Generals of Odisha, retired IAS officers of the rank of Chief Secretary/Secretaries to Government of India, retired IPS officers of the rank of D.G. of Police and former Principal Chief Conservators of Forests, Odisha, senior accredited media persons, etc. are normally invited to this event. Senior State Government and Central Government officers stationed at Bhubaneswar and Cuttack are also invited.</p>
                <p>Normally, an At Home party continues for one hour in the afternoon. The party begins with the arrival of His Excellency and the First Lady at the venue while six buglers blow their bugles. Soon after the arrival of His Excellency, the National Anthem is played. At the end of the party, the same procedure is followed. During the party, His Excellency goes around and meets the guests personally. All the guests are treated with high tea. Occasionally, a brief cultural programme is staged during this event. This event gives an opportunity to the people from different walks of life to get together on a single platform.</p>
               
