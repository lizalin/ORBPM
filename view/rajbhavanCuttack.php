                 <p>Situated on the bank of the Kathjodi river, the Lal Bagh Palace at Cuttack has a long and colourful history. This building witnessed the rise and fall of several rulers who controlled the fortune of Odisha. It was constructed by the Mughal Subedar stationed at Cuttack. Subsequently, the property passed into the hands of the Marathas. Over the years, the premises have undergone several alterations and modifications.</p>
                <p>William Bruton visited Cuttack in 1633, when the Lal Bagh Palace was under construction. In 1741, Saulat Jung, the Naib Nazim, took up residence in the palace. The building was occupied by the Naib Nazims till 1751 and by the representatives of the Bhonslas of Nagpur from 1751 to 1803. Lal Bagh came into the possession of the British in 1803 when Colonel Harcourt’s men defeated the Maratha soldiers. The Lal Bagh Palace was apparently leased out but again came into the possession of the government, who sold it in January 1862, and the purchaser sold the estate along with the building to the East India Irrigation Company. In 1863, the building came into the possession of the Government when they took over the irrigation works from the Company. Since 1868 the building was occupied by Commissioners and sometimes by collectors.</p>
                <p>In 1896, Shri R.C. Dutt, the then Commissioner, who was also a well-known historian, lived in this building. In a letter to his daughter, he describes the building as “the best-situated Commissioner’s house.” The building which was still under the Irrigation Branch was transferred to the Buildings and Roads Branch of the Government in 1914. In 1941, Shri K.C. Gajapati Narayan Deo, Maharaja of Paralakhemundi and Premier of Odisha, fixed his residence at the Lal Bagh palace for some time.</p>
                <p>On 18th July 1942, the Lal Bagh Palace became the new Government House. Sir Hawthorne Lewis was the first Governor to live in the Lal Bagh palace. This historic building, which stood witness to countless political and social upheavals during Mughal, Maratha and British rule in Odisha, became the center of administration.</p>
                <p>The Lal Bagh Palace continued to serve as the residence of the Governor till 1960. During the tenure of Shri Sukthankar in 1960, the Raj Bhavan was shifted from Cuttack to Bhubaneswar. Shri Sukthankar generously donated the building to the Indian Red Cross Society to utilize it as a children’s hospital. The Government of Orissa [now, Odisha] took over this hospital in 1966 and made it an independent institute for post-graduate training and research. At present, the institute is known as Sardar Vallabhbhai Patel Post Graduate Institute of Pediatrics, and is popularly known as Shishu Bhavan.
